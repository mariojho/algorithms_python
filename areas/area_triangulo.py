''' Cálculo del área de un triángulo '''
base_triangulo = 0; altura_triangulo = 0; area_triangulo = 0

base_triangulo = float(input("Dame la base del triángulo: "))
altura_triangulo = float(input("Dame la altura del triángulo: "))
area_triangulo = (base_triangulo * altura_triangulo) / 2

print("El área del triángulo es: ", area_triangulo, "\n")